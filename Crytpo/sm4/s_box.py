#32-shifted
def SHL(x, n):
    xx = int(int(x << n) & 0xffffffff)
    return xx

#32-circular shift
def ROTL(x, n):
    xx = SHL(x, n)
    yy = xx | int((x >> (32 - n)) & 0xffffffff)
    return yy

#input 32bits-NLT&LT-output 32bits
def LT(n):
    b=[0,0,0,0]
    a=[int((n>>24)&0xff), int((n>>16)&0xff), int((n>>8)&0xff), int((n)&0xff)]
    b=[SboxTable[a[0]],SboxTable[a[1]],SboxTable[a[2]],SboxTable[a[3]]]
    bb=int((b[0] << 24) | (b[1] << 16) | (b[2] << 8) | (b[3]))
    c = bb ^ (ROTL(bb, 2)) ^ (ROTL(bb, 10)) ^ (ROTL(bb, 18)) ^ (ROTL(bb, 24))
    return c

